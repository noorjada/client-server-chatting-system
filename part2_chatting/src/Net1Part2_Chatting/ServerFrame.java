    class ReadMessage extends Thread {

        Socket s;
        String ID;

        ReadMessage(Socket s, String username) {
            this.s = s;
            this.ID = username;

        }

        public void run() {

            while (!clientsHash.isEmpty() && clientsHash.containsKey(ID)) {
                try {
                    String in = new DataInputStream(s.getInputStream()).readUTF();
                    if (in.contains("logout")) {
                       
                        new DataOutputStream(((Socket) clientsHash.get(ID)).getOutputStream()).writeUTF("logout");////////
                        
                        clientsHash.remove(ID);
                        lastActiveTime.remove(ID); // Remove last active time
                         statusMap.remove(ID); // Remove status
                        addTextToArea(ID, false,null);
                        new endToEndList().start();

                  ////////////////////
                     }  else if (in.contains("set_status")) {
                    String[] parts = in.split(":");
                    String newStatus = parts[1];
                    statusMap.put(ID, newStatus);
                    addTextToArea(ID, false, newStatus); // Update the text area with the new status
                    new endToEndList().start();
                }  else {
                        lastActiveTime.put(ID, LocalDateTime.now()); // Update last active time
                    }///////////////////////
                } catch (IOException | BadLocationException ex) {
                    clientsHash.remove(ID);
                    lastActiveTime.remove(ID); // Remove last active time
                   statusMap.remove(ID); // Remove status
                    try {
                        addTextToArea(ID, false);
                    } catch (BadLocationException ex1) {
                        Logger.getLogger(ServerFrame.class.getName()).log(Level.SEVERE, null, ex1);
                    }
                    new endToEndList().start();
                    ex.printStackTrace();

                }
            }
        }
    }

