    private void addTextToArea(String username, boolean color, String status) throws BadLocationException {
    if (color) {
        // In login
        StyledDocument doc = textPaneArea.getStyledDocument();
        Style style = textPaneArea.addStyle("", null);
        StyleConstants.setForeground(style, Color.BLUE);
        StyleConstants.setBackground(style, Color.white);
        String s1 = username + " (" + status + ") logged in" + "\n";
        doc.insertString(doc.getLength(), s1, style);
    } else {
        // In logout or status change
        StyledDocument doc = textPaneArea.getStyledDocument();
        Style style = textPaneArea.addStyle("", null);
        if (status == null) {
            // Logout
            StyleConstants.setForeground(style, Color.RED);
            StyleConstants.setBackground(style, Color.white);
            String s1 = username + " logged out" + "\n";
            doc.insertString(doc.getLength(), s1, style);
        } else {
            // Status change
            switch (status) {
                case "Active":
                    StyleConstants.setForeground(style, Color.BLUE);
                    break;
                case "Busy":
                    StyleConstants.setForeground(style, Color.ORANGE);
                    break;
                case "Away":
                    StyleConstants.setForeground(style, Color.GRAY);
                    break;
            }
            StyleConstants.setBackground(style, Color.white);
            String s1 = username + " is now " + status + "\n";
            doc.insertString(doc.getLength(), s1, style);
        }
    }
}

